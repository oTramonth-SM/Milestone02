#include "libft.h"

int	ft_atoi(const char *str)
{
	int	c;
	int	s;
	int	i;

	c = 0;
	s = 1;
	i = 0;
	while (str[c] == '\t' || str[c] == '\n' || str[c] == ' '
		|| str[c] == '\r' || str[c] == '\f' || str[c] == '\v')
		c++;
	if (str[c] == '-' || str[c] == '+')
	{
		if (str[c] == '-')
			s = -1;
		c++;
	}
	while (str[c] >= '0' && str[c] <= '9')
	{
		i = ((i * 10) + (str[c] - '0'));
		c++;
	}
	return (s * i);
}
